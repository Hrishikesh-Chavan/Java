public class Parsing {
    public static void main(String[] args) {
        // Integer to String
        int n = 123;
        String str = Integer.toString(n);
        System.out.println(str.length());

        // String to Integer
        String str1 = "123";
        int n1 = Integer.parseInt(str1);
        int a=2;
        System.out.println(n1+a);  
    }
}
